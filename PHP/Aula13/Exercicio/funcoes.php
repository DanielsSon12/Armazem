<?php
function soma(float $n1, float $n2): float{
    return ($n1 + $n2);
}
function subtracao(float $n1, float $n2): float{
    return ($n1 - $n2);
}
function multiplicacao(float $n1, float $n2): float{
    return ($n1 * $n2);
}
function divisao(float $n1, float $n2): float{
    return ($n1 / $n2);
}
?>